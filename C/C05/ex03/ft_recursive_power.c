int	ft_recursive_power(int nb, int power)
{
	int	res;

	res = 1;
	if (power < 0)
		return (0);
	if (nb == 0 && power == 0)
		return (1);
	if (power-- != 0)
		res = nb * ft_recursive_power(nb, power);
	return (res);
}
