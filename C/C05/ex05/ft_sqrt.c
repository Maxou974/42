int	ft_sqrt(int nb)
{
	unsigned int	i;
	unsigned int	a;

	i = 0;
	a = nb;
	if (nb <= 0 || nb == 2147483647)
		return (0);
	while (i * i < a)
		i++;
	if (i * i == a)
		return (i);
	else
		return (0);
}
