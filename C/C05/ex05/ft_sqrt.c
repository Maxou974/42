int	ft_sqrt(int nb)
{
	int	i;

	i = 0;
	if (nb <= 0 || nb == 2147483647)
		return (0);
	while (i * i != nb)
	{
		if (i > nb / 3)
			return (0);
		i++;
	}
	return (i);
}

int main()
{
	printf("%d", ft_sqrt(2147483646));
}
