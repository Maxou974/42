int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;
	unsigned int	j;

	i = 0;
	j = 0;
	while (s1[i] && s1[i] == s2[i] && n)
	{
		i++;
		j++;
		n--;
	}
	if (n == 0)
		return (0);
	return (s1[i] - s2[i]);
}
